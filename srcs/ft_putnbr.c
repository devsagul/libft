#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == 0)
		ft_putchar('0');
	else
	{
		if (n < 0)
			ft_putchar('-');
		if (n / 10 != 0)
		{
			if (n < 0)
				ft_putnbr(-(n / 10));
			else
				ft_putnbr(n / 10);
		}
		if (n < 0)
			ft_putchar('0' - n % 10);
		else
			ft_putchar('0' + n % 10);
	}
}
